# Python program to check if a value is odd or even.
# A number is even if when divided by 2 gives a remainder of 0.
# If the remainder is 1, it is an odd number.

#Prompt user to enter the first value in pounds
print("Enter the first value ")
pound1= float(input())

#prompt user to enter the second value in pounds
print("Enter the first value")
pound2 = float(input())

 # odd or even for value 1
if (pound1 % 2) == 0:
   print("{0} is Even".format(pound1))
else:
   print("{0} is Odd".format(pound1))
   
#odd or even for value 1
